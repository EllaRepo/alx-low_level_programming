/**
 * op_add - adds two numbers
 * @a: first number
 * @b: second number
 *
 * Return: the sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
  * op_sub - subtracts two numbers
  * @a: first number
  * @b: second number
  *
  * Return: the difference of a and b
  */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies two nmbers
 * @a: first number
 * @b: second number
 *
 * Return: the produt of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides a by b
 * @a: first number
 * @b: second number
 *
 * Return: division of a by b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - computes remainder of the division a by b
 * @a: first number
 * @b: second number
 *
 * Return: a%b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
